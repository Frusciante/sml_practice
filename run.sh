#!/bin/sh

# 이 스크립트는 SML/NJ 인터프리터를 사용하여 SML 파일을 실행합니다.
# 인자로 실행할 .sml 파일의 경로를 받습니다.

# 파일명이 인자로 전달되었는지 확인합니다.
if [ -z "$1" ]; then
  echo "오류: 실행할 SML 파일이 지정되지 않았습니다."
  echo "사용법: ./run.sh <파일명.sml>"
  exit 1
fi

# 파일이 실제로 존재하는지 확인합니다.
if [ ! -f "$1" ]; then
  echo "오류: 파일을 찾을 수 없습니다: $1"
  exit 1
fi

# 'use' 명령어를 sml 인터프리터로 파이핑하여 파일을 실행합니다.
echo "use \"$1\";" | sml